
def subtract(x, y):
    results = x - y
    return results


def add(t, y):
    results = t + y
    return results


def multiply(k, y):
    results = k * y
    return results


def divide(m, y):
    results = m / y
    return results


subtract_results = subtract(10, 20)
print(subtract_results)

add_results = add(5, 10)
print(add_results)

multiply_results = multiply(9, 9)
print(multiply_results)

divide_results = divide(1302, 2)
print(divide_results)
